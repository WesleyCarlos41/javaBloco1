package herança;

public class animal {
		private String nome;
		private int idade;
		private boolean emitiSom;
		
		
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public boolean isEmitiSom() {
			return emitiSom;
		}
		public void setEmitiSom(boolean emitiSom) {
			this.emitiSom = emitiSom;
		}




}
